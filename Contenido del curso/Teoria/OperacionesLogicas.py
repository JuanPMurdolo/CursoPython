#Operaciones logicas

#Igual ==
valorUno = 5 == 9
print(valorUno)
#Diferente de !=
valorUno = 5 != 9
print(valorUno)
#Menor
valorUno = 5 < 9
print(valorUno)
#Mayor
valorUno = 5 > 9
print(valorUno)
#Menor igual <=
valorUno = 5 <= 9
print(valorUno)
#Mayor igual >=
valorUno = 5 >= 9
print(valorUno)

#operaciones logicas
print("Operaciones logicas and/or/not")
#And
valorUno = 10 > 1 and 11 < 9
print(valorUno)
#OR
valorUno = 10 > 1 or 11 < 9
print(valorUno)
#not
valorUno = not(10 > 1) and not(11<9)
print(valorUno)

