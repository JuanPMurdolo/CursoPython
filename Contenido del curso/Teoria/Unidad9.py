#Listas, Tuplas y Diccionarios

#Listas
#La lista es un tipo de coleccion ordenada. Seria equivalente a lo que en otros lenguajes se conoce por arrays, o vectores.

#Se usan corchetes[] y ,
lista = ["juan", "Pepe", "Rogelio"]


#Tuplas
#Una tupla es una secuencia de valores similar a una lista. La principal diferencia es que las tuplas son inmutables
#Se forma con parentesis () y ,
tupla = ("Jugar", 23.78, "Juan", "Tupla")

#Diccionarios
#Los diccionarios deben su nombre  a que son colecciones que relacionan una clave y un valor.
#Se forma con llaves {}, : en la clave y , para separar
diccionario = {"Uno": 1, "Dos": 2, "Tres": 3}
#Uno Dos y Tres serian Claves, 1 2 y 3 serian valores
print(diccionario["Uno"])
#Asi se imprime un diccionario